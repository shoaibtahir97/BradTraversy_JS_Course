/*
Data Types
1). Primitive Data Types:
    Stored directly in the location the variable access
    Stored on stack
    Types: 
    1).String
    2).Number 
    3).Boolean
    4).Undefined => Variable not assigned a value (declared but not initialize). 
                    All variables are undefined by default.
    5).Null => Intentional empty value
    6).Symbols(ES6) 

2). Reference Data Types:
    Accessed by reference
    Reference Types are Objects that are stored on the heap
    Objects have a pointer to the location in memory
    Types:
    1).Arrays
    2).Object 
    3).Functions
    4).Dates 

Data Types are associated with values and not the variable
Same Variable can hold multiple types.
No need to specify types
*/ 



